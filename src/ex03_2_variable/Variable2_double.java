//기본형 변수의 값을 출력해 봅니다.
//변수 : 단 하나의 값을 저장할 수 있는 메모리 공간
package ex03_2_variable;
public class Variable2_double {
	public static void main(String[] args) {
		
		System.out.println("=================실수형 변수=============");
		// 실수형 변수
		// f나 F를 숫자 뒤에 붙입니다.
		float ft = 3.14f;
		
		//d나 D를 숫자 뒤에 붙이거나 생략할 수 있습니다.
		double d = 42.195d;
		System.out.println("f=" + ft + " d=" + d); //f=3.14 d=42.195
	}
	
}
